x = 24
y = 69

# operasi penjumlahan
hasil = x + y
print ("nilai dari x + y = ", hasil)

# pengurangan
hasil = x - y
print ("nilai dari x - y = ", hasil)

# pembagian
hasil = x / y
print ("nilai dari x / y = ", hasil)

# perkalian
hasil = x * y
print ("nilai dari x * y = ", hasil)

# eksponen (perpangkatan)
hasil = x ** y
print ("nilai dari x ** y = ", hasil)

# modulus
hasil = x // y
print ("nilai dari x // y = ", hasil)

# floor division
hasil = x // y
print ("nilai dari x FD y = ", hasil)

# operasi prioritas
# 1. kerjakan dari kiri ke kanan 
# 2. perpangkatan perkalian/pembagian pengurangan/penambahan
